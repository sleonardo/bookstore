import SwiftUI

extension UIColor {
    /// Background  color
    static let launchBackground: UIColor = UIColor(named: "LaunchBackgroundColor")!
    static let mainBackground: UIColor = UIColor(named: "BackgroundColor")!
    /// Tab bar  color
    static let tabBarColor: UIColor = UIColor(named: "TabBarColor")!
    static let tabBarTitleColor: UIColor = UIColor(named: "SecondaryColor")!
}

extension Color {
    /// Background  color
    static let launchBackground: Color = Color("LaunchBackgroundColor")
    static let mainBackground: Color = Color("BackgroundColor")
    static let secondaryColor: Color = Color("SecondaryColor")
    /// Text  color
    static let textPrimaryColor: Color = Color("PrimaryColor")
    static let textSecondaryColor: Color = Color("SecondaryColor")
    static let titleColor: Color = Color("TitleColor")
    /// Color for available content
    static let unavailableColor: Color = Color("UnavailableColor")
    static let availableColor: Color = Color("AvailableColor")
    /// Button color
    static let buttomBorderColor: Color = Color("SecondaryColor")
    /// Gradient for active button
    static let buttomFillGradientB1Color: Color = Color("GradientB1")
    static let buttomFillGradientB2Color: Color = Color("GradientB2")
    /// Gradient for inactive button
    static let buttomFillGradientBD1Color: Color = Color("GradientBD1")
    static let buttomFillGradientBD2Color: Color = Color("GradientBD2")
    static let buttomFillGradientBD3Color: Color = Color("GradientBD3")
    /// Color icon Search
    static let imageIconColor: Color = Color("ColorIcon")
    static let imageIcon2Color: Color = Color("ColorIcon2")
    
}
